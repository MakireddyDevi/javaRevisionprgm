package com;

import java.util.Scanner;

public class IncrementPrgm {

	public static void main(String[] args) {
		
	Scanner sc= new Scanner(System.in);
	
	int a=sc.nextInt();
	
	System.out.println("enter the values ");
	System.out.println(++a);
	System.out.println(a--);
	
	System.out.println(a++);
	System.out.println(a);
	System.out.println(--a);
	System.out.println(++a);
	System.out.println(a++);
	System.out.println(a);
	
	
	
		
	}

}
